    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        try{
            String title = this.title.getText();
            String author = this.author.getText();
            String isbn = this.isbn.getText();
            String price = this.price.getText();
            String quantity = this.quantity.getText();
            String year = this.year.getText();
           
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/bsms","root","");
            Statement stm = con.createStatement();
            String sql = "insert into books (title,isbn,author,year,quantity,price) "
                    + "values('" + title + "','" + isbn + "','" + author +"','" + year + "','" + quantity + "','" + price +"')";
            stm.executeUpdate(sql);
            System.out.println("One recored added!");
                
            
        }catch(Exception ex){ System.out.println("Error:"+ex.getMessage());}
        
    }//GEN-LAST:event_saveActionPerformed

